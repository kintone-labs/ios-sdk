- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    if ([[segue identifier] isEqualToString:@"toListView"]) {
        KintoneViewSettingViewController *viewController = (KintoneViewSettingViewController *)[segue destinationViewController];
        viewController.selectedView = KintoneListViewSetting;
    }
    else if ([[segue identifier] isEqualToString:@"toDetailView"]) {
        KintoneViewSettingViewController *viewController = (KintoneViewSettingViewController *)[segue destinationViewController];
        viewController.selectedView = KintoneDetailViewSetting;
    }
}

